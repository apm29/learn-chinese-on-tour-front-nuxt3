    <p>The test will last for 105 minutes in total (including 5 minutes in which the test takers fill in personal
      information).</p>

    <h3>1. Listening Comprehension</h3>
    <p>
      There are 10 items in Part I. The recording of each item will be played only once. For each item, a short passage
      will read out, followed by an assertion which also appears on the test paper. The test taker should decide whether
      the assertion is true or false.
    </p>
    <p>
      There are 15 items in Part II. The recording of each item will be played only once. Each item consists of a
      dialogue between two persons. A third person will then ask a question based on the dialogue. There will be four
      possible answers on the test paper, from which test takers can choose the right one based on what they hear.
    </p>
    <p>
      There are 20 items in Part III. The recording of each item will be played only once. This section consists of a
      4-5 sentence dialogue or monologue followed by one or two questions. Four possible answers will be provided on the
      test paper, from which the test taker can choose the best one.
    </p>

    <h3>
      2. Reading Comprehension
    </h3>
    <p>
      There are 10 items in Part I. One or two sentences will be provided for each item, in which one word/phrase is
      missing. The test taker should choose one of the suggested words/phrases to fill in the blank.
    </p>
    <p>
      There are 10 items in Part II. Each item consists of three sentences that the test taker needs to arrange in the
      correct order.
    </p>
    <p>
      There are 20 items in Part III. Each item consists of a short passage, and one or two questions will be asked
      about the passage. The test taker needs to select the best answer out of the four answers provided.
    </p>
    <h3>3. Writing</h3>
    <p>
      There are 10 items in Part I. Each item consists of several words. The test taker should construct a sentence
      using these words.
    </p>
    <p>
      There are 5 items in Part II. Each item consists of a picture and a word. The test taker needs to write a sentence
      based on the picture and the word.
    </p>
    <h3>III. Results Certificate</h3>
    <p>
      There are 5 items in Part I. Each item lists several words. The test taker should construct a sentence using the
      words provided.
    </p>
    <p>
      There are 5 items in Part II. Each item provides a sentence with several blanks. The test taker should fill in the
      blanks with the appropriate characters.
    </p>
    <p>
      For the HSK (Level III), four results will be provided including listening, reading, writing and the total. The
      test taker must score at least 180 points to be considered passing.
    </p>
