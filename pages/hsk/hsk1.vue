    <h3>1. Listening Comprehension</h3>
    <p>
      There are 5 items in Part I. The recording of each item will be played twice. A phrase and picture will be
      provided
      with each item. The test takers should make a true-or-false judgment based on what they hear.
    </p>
    <p>
      There are 5 items in Part II. The recording of each item will be played twice. A sentence and 3 pictures will be
      provided with each item. The test takers should choose corresponding pictures based on what they hear.
    </p>
    <p>
